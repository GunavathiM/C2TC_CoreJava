package day2;

public class Arithmetic {

	public static void main(String[] args) {
		int a=3;
		int b=5;
		int sum=a+b;
		System.out.println("Addition:"+sum);		
		int difference = a-b;
		System.out.println("Subractionn:"+difference);
		int production = a*b;
		System.out.println("Multiplication:"+production);
		int quotient = a/b;
		System.out.println("Division:"+quotient);
		int remainder = a%b;
		System.out.println("Modulo:"+remainder);

	}

}
