package day2;

public class IfElseLadder {
public static void main(String[]args) {
	int a=20;
	if(a==10) 
		System.out.println("a is 10");
	
	else if(a==15) 
		System.out.println("a is 15");
	else if(a==20)
		System.out.println("a is 20");
	else
		System.out.println("a is not present");
}
}
